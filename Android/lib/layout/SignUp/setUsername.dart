    String token,
    String userId,
  ) async {
    Map data = {'username': username, 'email': email, 'x-auth-token': token};
    var jsonData = null;
    Map mapResponse;
    Map dataResponse;
    var response = await http.post(
      Uri.parse(
          //"http://habibsw-env-1.eba-rktzmmab.us-east-1.elasticbeanstalk.com/api/signUp/setUsername"
          "http://localhost:10.0.2.2/signUp/setUsername"),
      body: data,
      headers: {"x-auth-token": token},
    );
    if (response.statusCode == 201) {
      mapResponse = json.decode(response.body);
      dataResponse = mapResponse;
      userId = dataResponse['data']['userId'];
      print('this is user id');
      print(userId);
      setState(() {
        Navigator.of(context).pushAndRemoveUntil(
            MaterialPageRoute(
